package cw.weather;

public class WeatherVo {
	private int w_num;
	private String w_hum;
	private String w_temp;
	private int w_building;
	private int w_classroom;
	private String w_time;
	
	
	public int getW_num() {
		return w_num;
	}
	public void setW_num(int w_num) {
		this.w_num = w_num;
	}
	public String getW_hum() {
		return w_hum;
	}
	public void setW_hum(String w_hum) {
		this.w_hum = w_hum;
	}
	public String getW_temp() {
		return w_temp;
	}
	public void setW_temp(String w_temp) {
		this.w_temp = w_temp;
	}
	public int getW_building() {
		return w_building;
	}
	public void setW_building(int w_building) {
		this.w_building = w_building;
	}
	public int getW_classroom() {
		return w_classroom;
	}
	public void setW_classroom(int w_classroom) {
		this.w_classroom = w_classroom;
	}
	public String getW_time() {
		return w_time;
	}
	public void setW_time(String w_time) {
		this.w_time = w_time;
	}
	
}
